package com.servicestcg.to;

public class CartaClienteTO {
	private long cartaId;
	private long clienteId;
	private long tipoListaId;
	
	public long getCartaId() {
		return cartaId;
	}
	
	public void setCartaId(long cartaId) {
		this.cartaId = cartaId;
	}
	
	public long getClienteId() {
		return clienteId;
	}
	
	public void setClienteId(long clienteId) {
		this.clienteId = clienteId;
	}
	
	public long getTipoListaId() {
		return tipoListaId;
	}
	
	public void setTipoListaId(long tipoListaId) {
		this.tipoListaId = tipoListaId;
	}
	
	
}
